public class Dog {
    private String name;
    private int age;

//    default constructor
    public Dog(){}

//    all args constructor
    public Dog(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void sound() {
        System.out.println("Wof-wof");
    }


}
